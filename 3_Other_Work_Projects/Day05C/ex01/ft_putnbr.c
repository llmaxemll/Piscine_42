void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long nbb;

	nbb = nb;
	if (nbb < 0)
	{
		ft_putchar('-');
		nbb = nbb * -1;
	}
	if (nbb / 10)
		ft_putnbr(nbb / 10);
	ft_putchar((nbb % 10) + 48);
}
