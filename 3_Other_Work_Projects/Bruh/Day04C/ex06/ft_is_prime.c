int		ft_is_prime(int nb)
{
	int x;

	x = 2;
	if (nb > 1)
	{
		while ((nb % x) != 0)
		{
			x++;
		}
	}
	if (nb == x)
	{
		return (1);
	}
	else if (nb != x)
	{
		return (0);
	}
	return (nb);
}

int main(void)
{
	printf("%d", ft_is_prime(9));
	return (0);
}
