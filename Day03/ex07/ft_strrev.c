#include <stdio.h>

char 	*ft_sterrev(char *str)
{
	int		x;
	int		y;
	char	c;

	x = 0;
	while(str[x] != '\0')
		x++;

	y = 0;
	while (x >= 0)
	{
		c = str[x];
		str[x] = str[y];
		str[y] = c;
		x--;
		y++;
	}
	return(str);
}
