int	ft_sqrt(int nb)
{
	int x;
	int y;

	x = 0;
	y = 0;
	if (nb >= 2147483642)
		return (0);
	if (nb <= 0)
		return (0);
	if (y < nb)
	{
		while (y < nb)
		{
			y = x * x;
			if (y != nb)
				x++;
		}
	}
	return (x);
}
